//various datatypes of var,let and const are as follows
/*
1.STRING -holds words
2.NUMBER -numeric datatype
3.ARRAY - collection of similar datatypes
4.BOOLEAN - true or false
5.OBJECT - collection of unsimilar datatypes
6.NULL - displays null and not equals to 0
7.UNDEFINED - leaving any variable or let or cons undefined
*/ 


//string
var name="minu";
console.log("name is",name);

//number
var num=23;
console.log("number:",num);

//array
var array=["data","information","files"];
console.log("ARRAY:",array);

//object
var object={
    name:"raj",
    age:23,
    nationality:"india"
}
console.log("OBJECT:",object);

//boolean - true or false
var correct=true;
console.log("THE ANSWER IS THUS:",correct);

//null
var oops=null;
